    /**
     * Update the user's role.
     */
    public function changeRole(Request $request, User $user)
    {
        $request->validate(['role' => 'required|in:student,teacher,admin']);
        $user->role = $request->role;
        $user->save();
        return back()->with('success', 'Роль обновлена.');
